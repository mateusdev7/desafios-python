n1 = int(input('Digite um valor: '))
n2 = int(input('Digite outro valor: '))
s = n1 + n2
m = n1 * n2
d = n1 / n2
di = n1 // n2
e = n1 ** n2
r = n1 % n2
print(f"A soma vale: {s} \nO produto vale: {m} \nA divisão vale: {d:.3f}")
print(f"A divisão inteira vale: {di} \nA potência vale: {e}")
print(f"O resto da divisão vale: {r}")
